package pracitce;

public class Sum {
    public static void main(String[] args) {
        int r = 0;
        int sum = 0;
        for (int i = 0; i<=r;i++){
            if (i%2 != 0){
                sum+=i;
            }else {
                System.out.println(sum);
            }
            i++;
            System.out.println(sum);
        }


    }
}
